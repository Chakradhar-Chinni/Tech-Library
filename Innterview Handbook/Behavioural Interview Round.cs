Behavioural interviews assess how you’ve handled situations in the past to predict how you’ll act in the future.
Past behaviour is the best predictor of future performance.
  

Answer all questions in STAR format
S - Situation
T - Task 
A - Action 
R - Result

Describe the situation and the task. Exmplain what action was taken, result of the action
Communicate in positive approach (avoid negative or blaming scenario)
~2 mins long
Outcome-focused
With measurable impact (e.g., “reduced time by 30%”, “saved $90K/year”)


 Most behavioural questions are variants of the same 8–10 themes. 
| Category                | What They Look For               | Example Questions                                                                     |
| ----------------------- | -------------------------------- | ------------------------------------------------------------------------------------- |
| **Teamwork**            | Collaboration, empathy           | “Tell me about a time you worked with a difficult teammate.”                          |
| **Ownership**           | Accountability                   | “Describe a time you took full responsibility for a failure.”                         |
| **Problem-Solving**     | Analytical skills                | “Share an example of a complex problem you solved.”                                   |
| **Leadership**          | Initiative, influence            | “Tell me about a time you led without formal authority.”                              |
| **Adaptability**        | Flexibility                      | “Describe a situation where you had to learn something new quickly.”                  |
| **Conflict Management** | Handling differences             | “Tell me about a conflict and how you resolved it.”                                   |
| **Time Management**     | Prioritization                   | “How do you manage competing deadlines?”                                              |
| **Communication**       | Clarity, tone, stakeholder mgmt. | “Give an example of how you explained a technical concept to a non-technical person.” |


Example of properly answering:

Basic
“We had an issue and fixed it.
  
Good
“I helped fix the issue by checking logs.”

Excellent
“I identified the root cause using log analysis, fixed the issue, and implemented a retry mechanism reducing outages by 80%.”


**Teamwork** 
Describe a time when you had to handle a conflict at work.
Can you share an instance where you went over and above to help a team member?
Tell me about a time when your team achieved a significant goal.
Share an example of when you had to adapt to a team member’s working style.
How have you handled a situation where a team member was not pulling their weight?
Can you give an example of a time when your team disagreed with your decision, and how did you manage it?
How have you handled personality clashes in your team?
Talk about a time when you had to motivate a demotivated team member.
